class Film{
  final int id;
  final String title;
  final int year;
  final String synopsis;
  final String posterSrc;
  final String trailerSrc;
  final int duration;

  Film({
    required this.id,
    required this.title,
    required this.year,
    required this.synopsis,
    required this.posterSrc,
    required this.trailerSrc,
    required this.duration
  });
}